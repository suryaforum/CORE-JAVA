public class Lab9 {
    public static void main(String[] args) {
        int a, b, c;
        a = b = c = 0;
        System.out.println("a: " + a); // Output: a: 0
        System.out.println("b: " + b); // Output: b: 0
        System.out.println("c: " + c); // Output: c: 0

        String st1, st2, st3;
        st1 = st2 = st3 = "Surya";
        System.out.println("st1: " + st1); // Output: st1: Surya
        System.out.println("st2: " + st2); // Output: st2: Surya
        System.out.println("st3: " + st3); // Output: st3: Surya
    }
}
