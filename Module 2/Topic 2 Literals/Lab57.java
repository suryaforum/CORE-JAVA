public class Lab57 {
    public static void main(String[] args) {

        double d1 = 0Xap0;// 0Xap0 = 10 * 16^0 = 10
        System.out.println("Double d1: " + d1);

        double d2 = 0Xap1; // 0Xap1 = 10 * 16^1 = 160
        System.out.println("Double d2: " + d2);

        double d3 = 0Xap2; // 0Xap2 = 10 * 16^2 = 2560
        System.out.println("Double d3: " + d3);

        double d4 = 0XA9.0p0; // 0XA9.0p0 = 169 * 16^0 = 169
        System.out.println("Double d4: " + d4);

        double d5 = 0XA9.p0; // 0XA9.p0 = 169 * 16^0 = 169
        System.out.println("Double d5: " + d5);
    }
}
